#include "Point.hpp"

bool bsp(Point const a, Point const b, Point const c, Point const point)
{
	
    Fixed ProductABP = (b.getX() - a.getX()) * (point.getY() - a.getY()) - (b.getY() - a.getY()) * (point.getX() - a.getX());
    Fixed ProductBCP = (c.getX() - b.getX()) * (point.getY() - b.getY()) - (c.getY() - b.getY()) * (point.getX() - b.getX());
    Fixed ProductCAP = (a.getX() - c.getX()) * (point.getY() - c.getY()) - (a.getY() - c.getY()) * (point.getX() - c.getX());

    return (ProductABP > 0 && ProductBCP > 0 && ProductCAP > 0) ||
           (ProductABP < 0 && ProductBCP < 0 && ProductCAP < 0);
}